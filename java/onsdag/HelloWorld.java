public class HelloWorld {
	public static void main(String[] args) {

		// Write the text "Hello World!" to the screen
		System.out.println("Hello World!");

		// Calculate 2 + 2 and store the result in the varable four.
		int four = 2 + 2;

		// Write the result of the calculation to the screen.
		System.out.println("2+2 = " + four);

		// Get name from the input to the program
		String name = args[0];
		System.out.println("Your name is: " + name);
	}
}
